package com.cdpjosecabrera;

public class Calculadora {

public int suma(int num1, int num2)	{
	
	int suma=num1+num2;
	
	return suma;
}
public int resta(int num1, int num2)	{
	
	int resta=num1-num2;
	
	return resta;
}	
public int multi(int num1, int num2)	{
	
	int multi=num1*num2;
	return multi;
}	
public int div(int num1, int num2)	{
	
	int div=num1/num2;
	return div;
}	


}
